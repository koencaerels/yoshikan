declare(strict_types=1);

namespace App\YoshiKan\Application\Command\Member\MarkSubscriptionAsPayed;

class MarkSubscriptionAsPayed
{
    // —————————————————————————————————————————————————————————————————————————
    // Constructor
    // —————————————————————————————————————————————————————————————————————————

    private function __construct(
        protected int $id,
        protected string $type,
    ) {
    }

    // —————————————————————————————————————————————————————————————————————————
    // Hydrate from a json command
    // —————————————————————————————————————————————————————————————————————————

    public static function make(int $subscriptionId, string $type): self
    {
        return new self($subscriptionId, $type);
    }

    public static function hydrateFromJson(\stdClass $json): self
    {
        return new self(
            $json->id,
            $json->type
        );
    }

    // —————————————————————————————————————————————————————————————————————————
    // Getters
    // —————————————————————————————————————————————————————————————————————————

    public function getId(): int
    {
        return $this->id;
    }

    public function getType(): string
    {
        return $this->type;
    }
}
